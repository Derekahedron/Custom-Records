package derekahedron.customrecords.util;

import net.minecraft.resources.ResourceLocation;

public class CompatUtil {

    public static ResourceLocation alexsCaves(String id) {
        return ResourceLocation.fromNamespaceAndPath("alexscaves", id);
    }

    public static ResourceLocation inventoryExpansion(String id) {
        return ResourceLocation.fromNamespaceAndPath("invexp", id);
    }
}
